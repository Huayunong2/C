#include<stdio.h>
int main()
{
	char a = -1;
	//10000001 原码
	//11111110 反码
	//11111111 补码
	//11111111111111111111111111111111 整形提升原码
	//10000000000000000000000000000000 整形提升反码
	//10000000000000000000000000000001 整形提升补码
	signed char b = -1;
	//同上
	unsigned char c = -1;
	//00000000000000000000000011111111 整形提升原码
	//无符号则相同
	printf("%d %d %d", a, b, c);
	return 0;
}